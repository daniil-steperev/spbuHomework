package group343.stepyrev.set_tasks;

public class StringPair {
  private String fst;
  private String snd;

  public StringPair(String fst, String snd) {
    this.fst = fst;
    this.snd = snd;
  }

  public String getFst() {
    return fst;
  }

  public void setFst(String fst) {
    this.fst = fst;
  }

  public String getSnd() {
    return snd;
  }

  public void setSnd(String snd) {
    this.snd = snd;
  }
}
